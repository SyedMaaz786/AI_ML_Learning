# Python Basic Data Types 

# 1. Integer (int) 
num_int = 10
print(num_int, type(num_int))  

# 2. Float (float) 
num_float = 10.5
print(num_float, type(num_float))  

# 3. String (str) 
text = "Hello World"
print(text, type(text))

# 4. Boolean (bool) 
is_active = True
print(is_active, type(is_active))  

# 5. List (list) 
my_list = [1, 2, 3, 4]
print(my_list, type(my_list)) 

# 6. Tuple (tuple) 
my_tuple = (1, 2, 3)
print(my_tuple, type(my_tuple)) 

# 7. Set (set) 
my_set = {1, 2, 3, 3}
print(my_set, type(my_set))  

# 8. Dictionary (dict) 
my_dict = {"name": "Maaz", "age": 22}
print(my_dict, type(my_dict))

# 9. None Type (NoneType) 
nothing = None
print(nothing, type(nothing)) 

# 10. Complex numbers (complex)
num_complex = 3 + 5j
print(num_complex, type(num_complex)) 
